package de.sciss.interplay

import de.sciss.synth.io._

object DSP {
   def reverse( buf: Frames, off: Int, len: Int ) {
      buf.foreach { ch =>
         var i = off
         var j = off + len - 1
         while( i < j ) {
            val tmp = ch( i )
            ch( i ) = ch( j )
            ch( j ) = tmp
            i += 1; j -= 1
         }
      }
   }
}